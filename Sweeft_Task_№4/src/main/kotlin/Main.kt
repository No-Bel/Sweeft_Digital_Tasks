//4.მოცემულია String რომელიც შედგება „(„ და „)“ ელემენტებისგან.
// დაწერეთ ფუნქცია რომელიც აბრუნებს ფრჩხილები არის თუ არა მათემატიკურად სწორად დასმული.

var x = 0
var y = 0

fun main() {
    var str = "1+(2*5))"


    str.forEach {
        if (it == '(') {
            x++
        }
        if (it == ')') {
            y++
        }
    }

    if (x == y) {
        println("სწორად დასმული.")
    } else {
        println("არ არის სწორად დასმული.")
    }
}